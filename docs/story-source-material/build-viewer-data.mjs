import { readFile, writeFile } from "node:fs/promises";

const sourcePath = new URL("./story-source-extraction.json", import.meta.url);
const outputPath = new URL("./viewer-data.js", import.meta.url);
const data = JSON.parse(await readFile(sourcePath, "utf8"));

// The viewer only needs local image paths; keep private/temporary Google URLs out of its duplicate payload.
data.image_assets = data.image_assets.map(({ source_content_uri, ...asset }) => asset);

await writeFile(
  outputPath,
  `// Generated by build-viewer-data.mjs. Do not edit directly.\nwindow.STORY_SOURCE_DATA = ${JSON.stringify(data)};\n`,
  "utf8"
);
